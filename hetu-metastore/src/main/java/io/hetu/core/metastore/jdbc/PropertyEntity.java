package io.hetu.core.metastore.jdbc;

/**
 * property entity
 *
 * @since 2020-03-13
 */
public class PropertyEntity
{
    private String key;
    private String value;

    /**
     * construction
     */
    public PropertyEntity()
    {
    }

    /**
     * construction
     *
     * @param key   key of property entity
     * @param value value of property entity
     */
    public PropertyEntity(String key, String value)
    {
        this.key = key;
        this.value = value;
    }

    public String getKey()
    {
        return key;
    }

    public void setKey(String key)
    {
        this.key = key;
    }

    public String getValue()
    {
        return value;
    }

    public void setValue(String value)
    {
        this.value = value;
    }
}
